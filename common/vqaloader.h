#define CHUNK_CMDS MAKE_CHUNK('C', 'M', 'D', 'S') // Only used in Malcolms Revenge?

#define CHUNK_SPAN MAKE_CHUNK('S', 'P', 'A', 'N') // Only appears to be in LOLG/LOL3 Sound effect related

