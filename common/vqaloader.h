#define CHUNK_AESC MAKE_CHUNK('A', 'E', 'S', 'C') // Something Pal related          //Used in BR only?
#define CHUNK_LITE MAKE_CHUNK('L', 'I', 'T', 'E') // LIghTing Entries?              //Used in BR only?
